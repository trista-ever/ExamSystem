package org.dclab.model;

/**
 * 
 * Room info:
 *  id, name, size
 * 
 * @author zhaoz
 *
 */
public class RoomBean {

	private int id;
	private int size;
	private String name;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
